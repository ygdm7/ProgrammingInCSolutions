// Exercise 4 from chapter 3
// This program subtracts 15 from 87 and displays the result at the terminal.

#include<stdio.h>

int main( void )
{

  int value1, value2, result;

  value1 = 15;
  value2 = 87;
  result = 87 - 15;

  printf( "%i minus %i equals %i.\n", value2, value1, result );

  return 0;

}
